function HighlightCard({ itemKey, index }: { itemKey: string; index: number }) {
  const t = useTranslations('highlights.items');
  const ref = useRef(null);
  const isInView = useInView(ref, { once: true, margin: '-100px' });

  return (
    <motion.div
      ref={ref}
      initial={{ opacity: 0, y: 30 }}
      animate={isInView ? { opacity: 1, y: 0 } : { opacity: 0, y: 30 }}
      transition={{ duration: 0.6, delay: index * 0.1 }}
      className="luxury-card group flex flex-col items-center text-center"
    >
      <div className="mb-4 rounded-2xl bg-gradient-to-br from-primary-100 to-accent-100 p-4 text-primary-700 transition-transform duration-300 group-hover:scale-110">
        {highlightIcons[itemKey]}
      </div>
      <h3 className="mb-2 font-display text-lg font-semibold text-primary-800">
        {t(`${itemKey}.title`)}
      </h3>
      <p className="text-sm leading-relaxed text-primary-600">
        {t(`${itemKey}.description`)}
      </p>
    </motion.div>
  );
}

